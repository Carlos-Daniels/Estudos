"""
Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três
valores e o quadrado da soma dos três valores.
"""

num1 = float(input("digite o valor do primeiro numero: "))
num2 = float(input("digite o valor do segundo numero: "))
num3 = float(input("digite o valor do terceiro numero: "))

soma = num1 + num2 + num3

print(f"A soma de {num1} + {num2} + {num3} é igual a {soma}")